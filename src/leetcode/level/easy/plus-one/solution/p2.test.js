import plusOne from "./p2";

test.each([
    [[1, 2, 3], [1, 2, 4]],
    [[4, 3, 2, 1], [4, 3, 2, 2]],
    [[6, 1, 4, 5, 3, 9, 0, 1, 9, 5, 1, 8, 6, 7, 0, 5, 5, 4, 3], [6, 1, 4, 5, 3, 9, 0, 1, 9, 5, 1, 8, 6, 7, 0, 5, 5, 4, 4]],
    [[9, 9], [1, 0, 0]],
    [[8, 9, 9, 9], [9, 0, 0, 0]],
    [[9], [1, 0]]
])('plus one', (n, ans) => {
    expect(plusOne(n)).toEqual(ans);
});
