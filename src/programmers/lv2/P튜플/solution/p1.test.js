import solution from "./p1";

test.each([
    ["{{2},{2,1},{2,1,3},{2,1,3,4}}", [2, 1, 3, 4]],
    ["{{1,2,3},{2,1},{1,2,4,3},{2}}", [2, 1, 3, 4]],
    ["{{20,111},{111}}", [111, 20]],
    ["{{123}}", [123]],
    ["{{4,2,3},{3},{2,3,4,1},{2,3}}", [3, 2, 4, 1]],
])('튜플', (s, ans) => {
    expect(solution(s)).toEqual(ans);
});