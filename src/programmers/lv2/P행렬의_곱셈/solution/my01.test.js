const s = require('./my01');

test.each([
    [[[1, 4], [3, 2], [4, 1]], [[3, 3], [3, 3]], [[15, 15], [15, 15], [15, 15]]],
    [[[2, 3, 2], [4, 2, 4], [3, 1, 4]], [[5, 4, 3], [2, 4, 1], [3, 1, 1]], [[22, 22, 11], [36, 28, 18], [29, 20, 14]]]
])('행렬의 곱셈 테스트', (arr1, arr2, result) => {
    expect(s(arr1, arr2)).toStrictEqual(result);
});